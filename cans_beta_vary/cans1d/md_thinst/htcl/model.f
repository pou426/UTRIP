c======================================================================|
      subroutine model(ro,pr,gm,cool0,tecl0,decl0,x,ix
     &   ,mf_params)
c======================================================================|
      implicit double precision (a-h,o-z)
      dimension x(ix)
      dimension dxm(ix)
      dimension ro(ix),pr(ix)
c----------------------------------------------------------------------|
c   parameters
c----------------------------------------------------------------------|
      gm=5.d0/3.d0

      cool0=1.0d0
      tecl0=1.0d0
      decl0=1.d20
 
c----------------------------------------------------------------------|
c   grid
c----------------------------------------------------------------------|
      dx0=1.d0/real(ix)

      do i=1,ix
         dxm(i)=dx0
      enddo

      izero=ix/2
        x(izero)=-dxm(izero)/2
        do i=izero+1,ix
           x(i) = x(i-1)+dxm(i-1)
        enddo
        do i=izero-1,1,-1
           x(i) = x(i+1)-dxm(i)
        enddo

c----------------------------------------------------------------------|
c     initial condition
c----------------------------------------------------------------------|
      te0=10.d0

      do i=1,ix
         ro(i)  = 1.d0
         pr(i) = te0/gm
      enddo
c----------------------------------------------------------------------|
c     write parameters to file
c----------------------------------------------------------------------|
      
      return
      end
