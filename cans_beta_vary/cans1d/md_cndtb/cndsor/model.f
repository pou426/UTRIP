c======================================================================|
      subroutine model(ro,pr,gm,rkap0,margin,x,ix
     &     ,mf_params)
c======================================================================|
      implicit double precision (a-h,o-z)
      dimension x(ix)
      dimension dxm(ix)
      dimension ro(ix),pr(ix)
c----------------------------------------------------------------------|
c   parameters
c----------------------------------------------------------------------|
      gm=5.d0/3.d0

      rkap0=0.1d0
c----------------------------------------------------------------------|
c   grid
c----------------------------------------------------------------------|

      dx0=1.d0/real(ix-margin*2)
      do i=1,ix
         dxm(i)=dx0
      enddo

      izero=margin
        x(izero)=-dxm(izero)/2
        do i=izero+1,ix
           x(i) = x(i-1)+dxm(i-1)
        enddo
        do i=izero-1,1,-1
           x(i) = x(i+1)-dxm(i)
        enddo

c----------------------------------------------------------------------|
c     initial condition
c----------------------------------------------------------------------|

      wexp=0.3d0

      do i=1,ix
         ro(i) = 1.d0
         pr(i) = 1/gm*exp(-(x(i)/wexp)**2)
      enddo
c----------------------------------------------------------------------|
c     write parameters to file
c----------------------------------------------------------------------|
      
      return
      end
