c======================================================================|
      subroutine model(ro,vx,cs2,g0,margin,x,ix
     &     ,mf_params)
c======================================================================|
      implicit double precision (a-h,o-z)
c----------------------------------------------------------------------|
      dimension x(ix),dxm(ix)
      dimension ro(ix),vx(ix)

c----------------------------------------------------------------------|
c   parameters
c----------------------------------------------------------------------|
      cs2=1.d0

      g0=1.d0

c-----------------------------------------------------------------------
c     grid
c-----------------------------------------------------------------------

c-----------------------------------------------------------------------
c      dx,x

      dx0=4.d0/real(ix-margin*2)
      do i=1,ix
         dxm(i)=dx0
      enddo

      izero=ix/2+1
      x(izero)=dxm(izero)/2.d0

        do i=izero+1,ix
           x(i) = x(i-1)+dxm(i-1)
        enddo
        do i=izero-1,1,-1
           x(i) = x(i+1)-dxm(i)
        enddo

c-----------------------------------------------------------------------|
c   initial temperature, density, pressure distributions
c-----------------------------------------------------------------------|

      wexp=1.0d0
      amp=1.0d1

      do i=1,ix
         vx(i) = 0.d0
         ro(i) = amp*exp(-(x(i)/wexp)**2)
      enddo

c----------------------------------------------------------------------|
c     write parameters to file
c----------------------------------------------------------------------|
      call dacputparamd(mf_params,'csca2',csca2)
      call dacputparamd(mf_params,'amp',amp)

      
      return
      end
