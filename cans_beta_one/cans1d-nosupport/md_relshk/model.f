c======================================================================|
      subroutine model(ro,pr,vx,gm,margin,x,ix
     &     ,mf_params)
c======================================================================|
      implicit double precision (a-h,o-z)
c----------------------------------------------------------------------|
      dimension x(ix)
      dimension dxm(ix)

      dimension ro(ix),pr(ix),vx(ix)

c----------------------------------------------------------------------|
c   parameters
c----------------------------------------------------------------------|
      gm=5.d0/3.d0
c-----------------------------------------------------------------------
c     grid
c-----------------------------------------------------------------------
c      dxm,x

      dx0=100./real(ix-margin*2)
      do i=1,ix
         dxm(i)=dx0
      enddo
       
      izero=ix/2
      x(izero)=-dxm(izero)/2
      do i=izero+1,ix
         x(i) = x(i-1)+dxm(i-1)
      enddo
      do i=izero-1,1,-1
         x(i) = x(i+1)-dxm(i)
      enddo

c----------------------------------------------------------------------|
c     store initial condition into common area
c----------------------------------------------------------------------|

      do i=1,ix
         ro(i)=1.d0+(10.d0-1.d0)*0.5d0*(1.-tanh(x(i)))
         te   =1.d-6+(2.d0-1.d-6)*0.5d0*(1.-tanh(x(i)))
         pr(i)=(gm-1.d0)*ro(i)*te
         vx(i)=0.d0
         if (x(i).le.0.d0) then
         ro(i)=10.d0
         te   =2.d0
         pr(i)=(gm-1.d0)*ro(i)*te
         else
         ro(i)=1.d0
         te   =1.d-6
         pr(i)=(gm-1.d0)*ro(i)*te
         endif
      enddo
c----------------------------------------------------------------------|
c     write parameters to file
c----------------------------------------------------------------------|

      
      return
      end
