c======================================================================|
      subroutine model(ro,pr,gm,rkap0,margin,sc,scm,x,ix
     &     ,mf_params)
c======================================================================|
      implicit double precision (a-h,o-z)
c----------------------------------------------------------------------|
      dimension x(ix),dxm(ix)

      dimension sc(ix),scm(ix)

      dimension ro(ix),pr(ix)

c----------------------------------------------------------------------|
c   parameters
c----------------------------------------------------------------------|
      gm=5.d0/3.d0

      rkap0=1.d0
c-----------------------------------------------------------------------
c     grid
c-----------------------------------------------------------------------
      dx0=1.d0/real(ix-margin*2)

      do i=1,ix
         dxm(i)=dx0
      enddo

      izero=margin
      x(izero)=-dxm(izero)/2

        do i=izero+1,ix
           x(i) = x(i-1)+dxm(i-1)
        enddo
        do i=izero-1,1,-1
           x(i) = x(i+1)-dxm(i)
        enddo
c----------------------------------------------------------------------|
c       cross section of flux tube
c----------------------------------------------------------------------|

      do i=1,ix
        sc(i)=x(i)**2
        scm(i)=(x(i)+0.5d0*dxm(i))**2
      enddo

c-----------------------------------------------------------------------|
c   initial density, pressure distributions
c-----------------------------------------------------------------------|

      wexp=0.3d0

      do i=1,ix
         ro(i)  = 1.
         pr(i)  = 1/gm*exp(-(x(i)/wexp)**2)
      enddo
      
c----------------------------------------------------------------------|
c     write parameters to file
c----------------------------------------------------------------------|

      return
      end
